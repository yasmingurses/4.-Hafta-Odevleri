# Belirli listedeki elemanların toplamını bulmak için çalışıyor ancak kullanıcıdan başka elemanlı liste yaratması istenince toplamı bulmayı yapamadım

#Listedeki elemanların toplamını bulmak

toplam = 0
  
liste = [ 3 , 4 , 6 ,7 , 10 , 1, 50 ]  
  
for i in range(0, len(liste)): 
    toplam = toplam + liste[i] 
  
print("Listedeki elemanların toplamı: ", toplam) 





